// Sidebar.tsx - Navigation sidebar component
// Based on the electron-multi-gui-development.plan.md specifications

import React, { ReactNode } from 'react';

export interface SidebarItem {
  id: string;
  label: string;
  icon?: ReactNode;
  href?: string;
  onClick?: () => void;
  badge?: string | number;
  children?: SidebarItem[];
  disabled?: boolean;
  divider?: boolean;
}

interface SidebarProps {
  items: SidebarItem[];
  activeItem?: string;
  collapsed?: boolean;
  onItemClick?: (item: SidebarItem) => void;
  onToggleCollapse?: () => void;
  className?: string;
  header?: ReactNode;
  footer?: ReactNode;
}

const Sidebar: React.FC<SidebarProps> = ({
  items,
  activeItem,
  collapsed = false,
  onItemClick,
  onToggleCollapse,
  className = '',
  header,
  footer,
}) => {
  const handleItemClick = (item: SidebarItem) => {
    if (item.disabled) return;
    
    if (item.onClick) {
      item.onClick();
    } else if (onItemClick) {
      onItemClick(item);
    }
  };

  const renderItem = (item: SidebarItem, level: number = 0) => {
    const isActive = activeItem === item.id;
    const hasChildren = item.children && item.children.length > 0;
    const indentClass = level > 0 ? `ml-${level * 4}` : '';

    if (item.divider) {
      return (
        <div
          key={item.id}
          className="my-2 border-t border-gray-200 dark:border-gray-700"
        />
      );
    }

    return (
      <div key={item.id} className={indentClass}>
        <button
          onClick={() => handleItemClick(item)}
          disabled={item.disabled}
          className={`
            w-full flex items-center px-3 py-2 text-sm font-medium rounded-md transition-colors
            ${isActive
              ? 'bg-blue-100 dark:bg-blue-900 text-blue-700 dark:text-blue-300'
              : 'text-gray-700 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-700'
            }
            ${item.disabled ? 'opacity-50 cursor-not-allowed' : 'cursor-pointer'}
            ${collapsed ? 'justify-center' : 'justify-start'}
          `}
          title={collapsed ? item.label : undefined}
        >
          {item.icon && (
            <span className={`${collapsed ? '' : 'mr-3'} flex-shrink-0`}>
              {item.icon}
            </span>
          )}
          
          {!collapsed && (
            <>
              <span className="flex-1 text-left truncate">
                {item.label}
              </span>
              {item.badge && (
                <span className="ml-2 px-2 py-1 text-xs font-medium bg-blue-100 dark:bg-blue-900 text-blue-700 dark:text-blue-300 rounded-full">
                  {item.badge}
                </span>
              )}
            </>
          )}
        </button>

        {/* Render children if not collapsed */}
        {hasChildren && !collapsed && (
          <div className="ml-4 mt-1 space-y-1">
            {item.children!.map(child => renderItem(child, level + 1))}
          </div>
        )}
      </div>
    );
  };

  return (
    <div className={`h-full flex flex-col bg-white dark:bg-gray-800 ${className}`}>
      {/* Header */}
      {header && (
        <div className="p-4 border-b border-gray-200 dark:border-gray-700">
          {header}
        </div>
      )}

      {/* Toggle button */}
      {onToggleCollapse && (
        <div className="p-2 border-b border-gray-200 dark:border-gray-700">
          <button
            onClick={onToggleCollapse}
            className="w-full flex items-center justify-center p-2 text-gray-500 hover:text-gray-700 dark:text-gray-400 dark:hover:text-gray-200 transition-colors"
            title={collapsed ? 'Expand sidebar' : 'Collapse sidebar'}
          >
            <svg
              className={`w-5 h-5 transition-transform ${collapsed ? 'rotate-180' : ''}`}
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M11 19l-7-7 7-7m8 14l-7-7 7-7"
              />
            </svg>
          </button>
        </div>
      )}

      {/* Navigation items */}
      <nav className="flex-1 p-4 space-y-1 overflow-y-auto">
        {items.map(item => renderItem(item))}
      </nav>

      {/* Footer */}
      {footer && (
        <div className="p-4 border-t border-gray-200 dark:border-gray-700">
          {footer}
        </div>
      )}
    </div>
  );
};

